/**
 * Rasterize floor PDFs → huge PNGs (HVAC / plan style) for 2D + 3D maps.
 *
 *   npm run maps:pdf
 *
 * Edit FLOOR_SPECS below to point at your PDFs. Requires: mupdf (devDependency).
 */
import mupdf from "mupdf";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const OUT_DIR = path.join(ROOT, "public", "maps");
const MAX_WIDTH = Number(process.env.MAP_MAX_WIDTH || 7200);

/** Ground = HVAC-style sheet; First = upper level (swap PDFs if you want). */
const FLOOR_SPECS = [
  {
    key: "ground",
    pdf: path.join(ROOT, "public", "level- 1F HVAC.pdf"),
    outPng: "floor-ground.png",
  },
  {
    key: "first",
    pdf: path.join(ROOT, "public", "2nd floor.pdf"),
    outPng: "floor-first.png",
  },
];

fs.mkdirSync(OUT_DIR, { recursive: true });

const dims = {};

for (const spec of FLOOR_SPECS) {
  if (!fs.existsSync(spec.pdf)) {
    console.warn(`Skip ${spec.key}: missing ${spec.pdf}`);
    continue;
  }
  console.log(`Rendering ${spec.key} ← ${path.basename(spec.pdf)} …`);
  const doc = mupdf.Document.openDocument(spec.pdf);
  const page = doc.loadPage(0);
  const b = page.getBounds();
  const pw = b[2] - b[0];
  const ph = b[3] - b[1];
  const scale = MAX_WIDTH / pw;
  const matrix = mupdf.Matrix.scale(scale, scale);
  const pix = page.toPixmap(matrix, mupdf.ColorSpace.DeviceRGB, false);
  const w = pix.getWidth();
  const h = pix.getHeight();
  const png = Buffer.from(pix.asPNG());
  const outPath = path.join(OUT_DIR, spec.outPng);
  fs.writeFileSync(outPath, png);
  pix.destroy?.();
  page.destroy?.();
  doc.destroy?.();
  dims[spec.key] = { w, h };
  console.log(`  → ${spec.outPng} (${w}×${h}) ${(png.length / 1e6).toFixed(2)} MB`);
}

if (Object.keys(dims).length === 0) {
  console.error("No PDFs rendered. Add PDFs under public/ or fix paths in FLOOR_SPECS.");
  process.exit(1);
}

const ts = `/* eslint-disable -- auto-generated by npm run maps:pdf */
export const GENERATED_FLOOR_PLAN_PIXELS: Record<string, { w: number; h: number }> = ${JSON.stringify(dims, null, 2)};
`;
const genPath = path.join(ROOT, "src", "data", "floorPlanDimensions.generated.ts");
fs.writeFileSync(genPath, ts);
console.log(`Wrote ${genPath}`);
